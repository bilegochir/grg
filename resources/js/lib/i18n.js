const interpolate = (value, params) => Object.entries(params).reduce(
    (carry, [key, replacement]) => carry.replaceAll(`:${key}`, replacement ?? ''),
    value,
);

export function useLocale(locale = null) {
    const page = usePage();

    const resolvedLocale = computed(() => {
        const explicitLocale = locale?.value ?? locale;
        const pageLocale = page.props.app?.locale;
        const activeLocale = explicitLocale ?? pageLocale ?? 'en';

        return messages[activeLocale] ? activeLocale : 'en';
    });

    const t = (key, fallback = key, params = {}) => {
        const value = key
            .split('.')
            .reduce((carry, part) => carry?.[part], messages[resolvedLocale.value]);

        if (typeof value !== 'string') {
            return fallback;
        }

        return interpolate(value, params);
    };

    return {
        t,
        locale: resolvedLocale,
    };
}
